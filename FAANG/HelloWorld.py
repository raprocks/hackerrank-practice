n = int(input()) # take input and convert it to integer for
# range function to work

for _ in range(n): # _ is for a variable that wont be used
    print("Hello World \\n") # \\ prints \ as \n prints a newline

# we can also do the same problem as following
for _ in range(n):
    print(r"Hello World \n") #r"" is a raw string that prints just as you write

